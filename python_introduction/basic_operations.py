# إسناد القيم إلى المتغيرات
number1 = 10
number2 = 5

# إجراء العمليات الحسابية
sum_result = number1 + number2
difference_result = number1 - number2
product_result = number1 * number2

# طباعة النتائج
print(f"جمع {number1} و {number2} هو {sum_result}")
print(f"طرح {number1} و {number2} هو {difference_result}")
print(f"ضرب {number1} و {number2} هو {product_result}")
